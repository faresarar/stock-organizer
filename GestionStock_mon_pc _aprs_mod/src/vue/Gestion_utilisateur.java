    private void ButtonDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonDelActionPerformed
      int position =jTable1.getSelectedRow();
     
        if(position==-1) t.ShowFailMessage(this, "veuiller sélectionner un utilisateur pour supprimer");//JOptionPane.showMessageDialog(null, " veuiller sélectionner un utilisateur pour supprimer ","",JOptionPane.INFORMATION_MESSAGE,img2);
       else { 
         MessageDialog obj = new MessageDialog(this);
        obj.showMessage("Voulez vous supprimer ce utilisateur ?", "Si vous cliquer sur ok \nCe utilisateur sera suppimer .");
          //  int  result =JOptionPane.showConfirmDialog(null,"voulez vous supprimer  ce produit","",JOptionPane.YES_NO_OPTION);
          //  if(result==0){
          if (obj.getMessageType() == MessageDialog.MessageType.OK) {   
            
      
                if(ListUser.get(position).getUsername().equals("admin"))
                  t.ShowFailMessage(this, "Tu ne peut pas supprimer le compte admin !");
                  //  JOptionPane.showMessageDialog(null, " tu ne peut pas supprimer le compte admin ","",JOptionPane.INFORMATION_MESSAGE,img2);
                else {
                    try {
                        uop.sup_User(ListUser.get(position).getUsername());
                    } catch (Exception ex) {
                        Logger.getLogger(Gestion_utilisateur.class.getName()).log(Level.SEVERE, null, ex);
                    }
           ListUser.remove(position);
           uop.displayResult(ListUser, jTable1);
                      }
          
        } else {}
        
        }
    }//GEN-LAST:event_ButtonDelActionPerformed

