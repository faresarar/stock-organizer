    private void Bloquer_debloquerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bloquer_debloquerActionPerformed
        position =jTable1.getSelectedRow();
     
        if(position==-1)
             t.ShowFailMessage(this, "veuiller sélectionner un utilisateur pour bloquer/débloquer");
           // JOptionPane.showMessageDialog(null, " veuiller sélectionner un utilisateur pour bloquer/débloquer ","",JOptionPane.INFORMATION_MESSAGE,img2);
       else { if(ListUser.get(position).getUsername().equals("admin"))
           t.ShowFailMessage(this, "Tu ne peut pas changer l'etat d'utilisateur admin");
       else {try {
           int etat=ListUser.get(position).getEtat();
           String bloque_debloquer="active";
           if(etat==0){etat=1;bloque_debloquer="active";}
           else {etat=0; bloque_debloquer="inactive";}
           uop.modifier_Etat(etat, ListUser.get(position).getUsername());
           t.ShowGoodMessage(this, "L'utilisateur "+ListUser.get(position).getUsername()+" en etat "+bloque_debloquer);
           ListUser.get(position).setEtat(etat);
           uop.displayResult(ListUser, jTable1);
           } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), " Erreur ", JOptionPane.WARNING_MESSAGE, null);
               Logger.getLogger(Gestion_utilisateur.class.getName()).log(Level.SEVERE, null, ex);
           }
}
           
           }
    }//GEN-LAST:event_Bloquer_debloquerActionPerformed

